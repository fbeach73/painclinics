import type { Clinic, DayHours, OperatingHours } from '@/types/clinic';

// Standard hours helper
function createHours(
  weekdayOpen: string,
  weekdayClose: string,
  saturdayOpen?: string,
  saturdayClose?: string,
  sundayClosed = true
): OperatingHours {
  const weekdayHours: DayHours = { open: weekdayOpen, close: weekdayClose, closed: false };
  const saturdayHours: DayHours = saturdayOpen
    ? { open: saturdayOpen, close: saturdayClose!, closed: false }
    : { open: '', close: '', closed: true };
  const sundayHours: DayHours = sundayClosed
    ? { open: '', close: '', closed: true }
    : { open: '10:00 AM', close: '4:00 PM', closed: false };

  return {
    monday: weekdayHours,
    tuesday: weekdayHours,
    wednesday: weekdayHours,
    thursday: weekdayHours,
    friday: weekdayHours,
    saturday: saturdayHours,
    sunday: sundayHours,
  };
}

