// Helper to get clinic by slug
export function getClinicBySlug(slug: string): Clinic | undefined {
  return mockClinics.find((clinic) => clinic.slug === slug);
}

// Helper to get clinic by ID
export function getClinicById(id: string): Clinic | undefined {
  return mockClinics.find((clinic) => clinic.id === id);
}

// Helper to get featured clinics
export function getFeaturedClinics(): Clinic[] {
  return mockClinics.filter((clinic) => clinic.isFeatured);
}

// Helper to get clinics by city
export function getClinicsByCity(city: string): Clinic[] {
  return mockClinics.filter((clinic) =>
    clinic.address.city.toLowerCase() === city.toLowerCase()
  );
}
